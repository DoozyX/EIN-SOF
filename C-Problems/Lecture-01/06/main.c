#include <stdio.h>
/*
6. Напиши програм кој внесува петцифрен број, ги распределува сите цифри на бројот во посебни променливи и ги печати секоја од цифрите со растојание една од дуга од три празни места.
Прмер. Нека корисникот внел 42139. Програмот треба да испечати:
4   2   1   3   9
 */
int main() {
    int num5;
    scanf("%d", &num5);
    printf("%d   %d   %d   %d   %d", num5/10000, num5/1000%10, num5/100%10, num5%100/10, num5%10);
    return 0;
}