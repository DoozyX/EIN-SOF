#include <stdio.h>

/*
 * 2.      Напиши програма која на функцијата vreme и доделува три целобројни аргументи, час, минута и секунда и како резултат го враќа бројот на секунди до 12 часот.
 * Имено, ако е 11 часот 59 минути и 58 секунди, тогаш има уште 2 секунди до 12 часот.
 */

int vreme(int hours, int minutes, int seconds) {
    return (11-hours) * 3600 + (59-minutes) * 60 + 60 - seconds;
}
int main() {
    printf("%d\n", vreme(11,59,58));
    return 0;
}